const CURVE_25519_PRIME: [u8; 32] = [
    0xED, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F,
];

pub struct DhValidator;

impl DhValidator {
    pub fn validate_x25519_public_key(public_key: &[u8]) -> Result<(), ProtocolError> {
        if public_key.len() != X25519_PUBLIC_KEY_BYTES {
            return Err(ProtocolError::invalid_input(format!(
                "Invalid X25519 public key size: expected {}, got {}",
                X25519_PUBLIC_KEY_BYTES,
                public_key.len()
            )));
        }
        if Self::has_small_order(public_key) {
            return Err(ProtocolError::invalid_input(
                "X25519 public key is a small-order point (invalid for DH)",
            ));
        }
        if !Self::is_valid_curve25519_point(public_key) {
            return Err(ProtocolError::invalid_input(
                "X25519 public key is not a valid Curve25519 field element",
            ));
        }
        Ok(())
    }

    fn has_small_order(public_key: &[u8]) -> bool {
        let mut normalized = [0u8; 32];
        normalized.copy_from_slice(public_key);
        normalized[31] &= X25519_CLAMP_BYTE31_LOW;

        let mut found = false;
        for point in &SMALL_ORDER_POINTS {
            found |= bool::from(normalized.ct_eq(point));
        }
        found
    }

    fn is_valid_curve25519_point(public_key: &[u8]) -> bool {
        if public_key.len() != X25519_PUBLIC_KEY_BYTES {
            return false;
        }
        let mut borrow: u64 = 0;
        let mut i = 0;
        while i < 8 {
            let byte_offset = i * 4;
            let key_word = u64::from(u32::from_le_bytes([
                public_key[byte_offset],
                public_key[byte_offset + 1],
                public_key[byte_offset + 2],
                public_key[byte_offset + 3],
            ]));
            let prime_word = u64::from(u32::from_le_bytes([
                CURVE_25519_PRIME[byte_offset],
                CURVE_25519_PRIME[byte_offset + 1],
                CURVE_25519_PRIME[byte_offset + 2],
                CURVE_25519_PRIME[byte_offset + 3],
            ]));
            let diff = key_word.wrapping_sub(prime_word).wrapping_sub(borrow);
            borrow = (diff >> 63) & 1;
            i += 1;
        }
        borrow == 1
    }
}
